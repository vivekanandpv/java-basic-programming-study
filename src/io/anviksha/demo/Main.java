package io.anviksha.demo;


public class Main {
    public static void main(String[] args) {
        //  +=, -=, *=, /=, %=

        int i = 0;

        i+=5;   //  i = i + 5;

        i-=2;   //  i = i - 2;

        i*=4;   //  i = i * 4;

        i/=3;   //  i = i / 3;

        i%=2;   //  i = i % 2;
    }
}